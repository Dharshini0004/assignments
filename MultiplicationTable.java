package week2.day1;

public class MultiplicationTable {

	public static void main(String[] args) {
		int num = 9;
		for (int i = 1; i <= 10; i++) {
			int sum = i * num;
			System.out.println(i + " * " + num + " = " + sum);
		}

	}

}
